package cz.jednoadem.geochlast;

public class LocationRecord {

	private Double lat;

	private Double lng;

	public void setLat(Double l) {
		this.lat = l;
	}

	public void setLng(Double l) {
		this.lng = l;
	}

	public Double getLat() {
		return this.lat;
	}

	public Double getLng() {
		return this.lng;
	}

}
